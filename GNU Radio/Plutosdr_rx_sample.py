    def closeEvent(self, event):
        self.settings = Qt.QSettings("gnuradio/flowgraphs", "Plutosdr_rx_sample")
        self.settings.setValue("geometry", self.saveGeometry())
        self.stop()
        self.wait()

        event.accept()

